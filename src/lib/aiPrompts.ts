/**
 * Get system prompt configuration for a specific role
 * @param role User role (principal, teacher, parent, student, grading)
 * @returns Role prompt configuration
 */
export function getRolePromptConfig(role: string): RolePromptConfig {
  const config = AI_ROLE_PROMPTS[role];
  if (!config) {
    // Default to student role if role not found
    return AI_ROLE_PROMPTS.student;
  }
  return config;
}

/**
 * Get system prompt for a specific role
 * @param role User role
 * @returns System prompt string
 */
export function getSystemPrompt(role: string): string {
  return getRolePromptConfig(role).systemPrompt;
}
